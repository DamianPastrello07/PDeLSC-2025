//Se crean las funciones de las operaciones
function suma(a,b){
    return a + b;
}

function resta(a,b){
    return a - b;
}

function multiplicacion(a,b){
    return a * b;
}

function division(a,b){
    return a / b;
}

//Se muestran los resultados en base a los valores de las operaciones
console.log("La suma de 4 y 5 es: " + suma(4,5));
console.log("La resta de 3 y 6 es: " + resta(3,6)); 
console.log("La multiplicación de 2 y 7 es: " + multiplicacion(2,7));
console.log("La división de 20 y 4 es: " + division(20,4));
